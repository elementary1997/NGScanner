package ru.ngscanner.obd

/**
 * Протоколы связи с ЭБУ, поддерживаемые ELM327 (команда `ATSP<code>`).
 *
 * Номера — из спецификации ELM327 и одинаковы у всех клонов. `ATDPN` возвращает
 * текущий номер в hex (при автоопределении — с префиксом «A», напр. «A6»), поэтому
 * коды `A`/`B`/`C` разбираются как 10/11/12.
 *
 * Зачем ручной выбор, если есть автоопределение ([AUTO]):
 * - На K-line (ISO 9141 / KWP) автопоиск медленный и на клонах часто «залипает»,
 *   упорно отдавая NO DATA на живом ЭБУ. Жёстко заданный протокол подключается сразу.
 * - Часть российских ЭБУ (ВАЗ/ГАЗ) отвечает только на конкретную инициализацию KWP.
 * - Автопоиск перебирает протоколы по кругу и тратит секунды на каждом соединении.
 */
enum class ObdProtocol(
    /** Код для `ATSP` (один hex-символ). */
    val code: String,
    val label: String,
    val about: String,
) {
    AUTO("0", "Автоопределение", "Адаптер сам переберёт протоколы. Медленно, но работает везде."),
    J1850_PWM("1", "SAE J1850 PWM", "41,6 кбит. Ford 1996–2004."),
    J1850_VPW("2", "SAE J1850 VPW", "10,4 кбит. GM 1996–2005."),
    ISO_9141_2("3", "ISO 9141-2 (K-line)", "Инициализация 5 бод. Азия и Европа 1996–2004."),
    KWP_5BAUD("4", "ISO 14230 KWP (5 бод)", "K-line с медленной инициализацией."),
    KWP_FAST("5", "ISO 14230 KWP (быстрая)", "K-line с быстрой инициализацией. Частый вариант у ВАЗ/ГАЗ."),
    CAN_11_500("6", "CAN 11 бит, 500 кбит", "ISO 15765-4. Большинство машин с 2008 года."),
    CAN_29_500("7", "CAN 29 бит, 500 кбит", "ISO 15765-4 с расширенными адресами."),
    CAN_11_250("8", "CAN 11 бит, 250 кбит", "ISO 15765-4 на пониженной скорости."),
    CAN_29_250("9", "CAN 29 бит, 250 кбит", "ISO 15765-4 с расширенными адресами."),
    J1939("A", "SAE J1939 (CAN 29 бит)", "Грузовые и спецтехника, 250 кбит."),
    USER1("B", "USER1 CAN 11 бит", "125 кбит, нестандартный."),
    USER2("C", "USER2 CAN 11 бит", "50 кбит, нестандартный."),
    ;

    /** Номер протокола, как его отдаёт `ATDPN` (hex-разбор кода). */
    val number: Int get() = code.toInt(16)

    /** CAN-семейство (ISO 15765 / J1939) — в отличие от легаси K-line и J1850. */
    val isCan: Boolean get() = this != AUTO && number >= CAN_11_500.number

    /**
     * Длина заголовка в hex-символах при включённых заголовках (`ATH1`) — нужна, чтобы
     * группировать ответы по ЭБУ (иначе кадры разных модулей сливаются в фантомные коды).
     * 11-битный CAN → 3 (`7Ex`), 29-битный → 8 (`18DAF1xx`), легаси → 0 (не группируем).
     */
    val headerHexLen: Int
        get() = when (this) {
            CAN_11_500, CAN_11_250, USER1, USER2 -> 3
            CAN_29_500, CAN_29_250, J1939 -> 8
            else -> 0
        }

    companion object {
        /** Протокол по номеру из `ATDPN`; `null` — 0 (не определён) или неизвестный номер. */
        fun fromNumber(n: Int?): ObdProtocol? {
            if (n == null || n == 0) return null
            return entries.firstOrNull { it != AUTO && it.number == n }
        }

        /** Протокол по коду настройки; неизвестный код → [AUTO] (безопасный дефолт). */
        fun fromCode(code: String?): ObdProtocol =
            entries.firstOrNull { it.code.equals(code, ignoreCase = true) } ?: AUTO

        /** Что перебирать при поиске рабочего протокола: сначала частые, затем редкие. */
        val PROBE_ORDER: List<ObdProtocol> = listOf(
            CAN_11_500, CAN_29_500, KWP_FAST, ISO_9141_2, KWP_5BAUD,
            CAN_11_250, CAN_29_250, J1850_VPW, J1850_PWM,
        )
    }
}
