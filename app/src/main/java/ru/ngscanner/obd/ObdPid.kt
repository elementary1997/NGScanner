package ru.ngscanner.obd

/**
 * Параметры OBD-II Mode 01 (SAE J1979): команда, подпись, единицы и декодер.
 * `decode` принимает байты данных (A = `[0]`, B = `[1]`, …) — то, что идёт
 * после заголовка ответа «41 XX». Формулы выверены по SAE J1979.
 */
enum class ObdPid(
    val cmd: String,
    val label: String,
    val unit: String,
    val decode: (IntArray) -> Double,
) {
    RPM("010C", "Обороты", "об/мин", { d -> (256 * d[0] + d[1]) / 4.0 }),
    SPEED("010D", "Скорость", "км/ч", { d -> d[0].toDouble() }),
    COOLANT("0105", "Темп. ОЖ", "°C", { d -> d[0] - 40.0 }),
    ENGINE_LOAD("0104", "Нагрузка", "%", { d -> d[0] * 100 / 255.0 }),
    INTAKE_TEMP("010F", "Темп. впуска", "°C", { d -> d[0] - 40.0 }),
    MAF("0110", "MAF", "г/с", { d -> (256 * d[0] + d[1]) / 100.0 }),
    THROTTLE("0111", "Дроссель", "%", { d -> d[0] * 100 / 255.0 }),
    STFT("0106", "Топл. коррекция", "%", { d -> d[0] / 1.28 - 100 }),
    TIMING("010E", "УОЗ", "°", { d -> d[0] / 2.0 - 64 }),
    MAP("010B", "MAP", "кПа", { d -> d[0].toDouble() }),
    FUEL_LEVEL("012F", "Топливо", "%", { d -> d[0] * 100 / 255.0 }),
    VOLTAGE("0142", "Напряжение", "В", { d -> (256 * d[0] + d[1]) / 1000.0 }),
}
