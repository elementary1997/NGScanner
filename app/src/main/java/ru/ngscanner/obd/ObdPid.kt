package ru.ngscanner.obd

/**
 * Параметры OBD-II Mode 01 (SAE J1979): команда, подпись, единицы, декодер,
 * а также диапазон шкалы, пороги «внимание/критично» и справка для UI.
 *
 * `decode` принимает байты данных (A = `[0]`, B = `[1]`, …) — то, что идёт
 * после заголовка ответа «41 XX». Формулы выверены по SAE J1979.
 *
 * Пороги нужны для цветовой сигнализации: значение красится в янтарь при
 * выходе за `warn*` и в красный при выходе за `crit*` (в любую сторону —
 * `*High` вверх, `*Low` вниз). `gaugeMax` задаёт полную шкалу прибора/бара.
 */
enum class ObdPid(
    val cmd: String,
    val label: String,
    val unit: String,
    val gaugeMax: Double,
    val warnHigh: Double? = null,
    val critHigh: Double? = null,
    val warnLow: Double? = null,
    val critLow: Double? = null,
    val about: String,
    val norm: String,
    val decode: (IntArray) -> Double,
) {
    RPM(
        "010C", "Обороты", "об/мин", gaugeMax = 7000.0, warnHigh = 5500.0, critHigh = 6500.0,
        about = "Частота вращения коленвала. На прогретом холостом ходу обороты ровные и " +
            "стабильные; «плавание» указывает на подсос воздуха, грязный дроссель или пропуски " +
            "воспламенения.",
        norm = "ХХ 700–900 об/мин",
        decode = { d -> (256 * d[0] + d[1]) / 4.0 },
    ),
    SPEED(
        "010D", "Скорость", "км/ч", gaugeMax = 240.0,
        about = "Скорость по датчику в коробке передач. На стоянке — 0. Расхождение со " +
            "спидометром указывает на неисправный датчик скорости.",
        norm = "по датчику ДСА",
        decode = { d -> d[0].toDouble() },
    ),
    COOLANT(
        "0105", "Темп. ОЖ", "°C", gaugeMax = 130.0, warnHigh = 107.0, critHigh = 115.0,
        about = "Температура охлаждающей жидкости. Рабочий диапазон достигается за 5–10 минут; " +
            "перегрев выше 110 °C опасен для двигателя.",
        norm = "85–105 °C",
        decode = { d -> d[0] - 40.0 },
    ),
    ENGINE_LOAD(
        "0104", "Нагрузка", "%", gaugeMax = 100.0, warnHigh = 85.0, critHigh = 95.0,
        about = "Расчётная нагрузка на двигатель. На холостых — низкая; резкий рост без нажатия " +
            "на газ говорит о лишнем сопротивлении или ошибке датчиков.",
        norm = "ХХ 15–35 %",
        decode = { d -> d[0] * 100 / 255.0 },
    ),
    INTAKE_TEMP(
        "010F", "Темп. впуска", "°C", gaugeMax = 90.0, warnHigh = 70.0, critHigh = 90.0,
        about = "Температура воздуха во впускном коллекторе. Влияет на расчёт смеси; сильно " +
            "завышенная температура снижает мощность.",
        norm = "близко к забортной, до ~60 °C",
        decode = { d -> d[0] - 40.0 },
    ),
    MAF(
        "0110", "MAF", "г/с", gaugeMax = 180.0,
        about = "Массовый расход воздуха — ключевой параметр для расчёта топливной смеси. " +
            "Заниженный поток на холостых — признак подсоса или загрязнённого ДМРВ.",
        norm = "ХХ 2–5 г/с",
        decode = { d -> (256 * d[0] + d[1]) / 100.0 },
    ),
    THROTTLE(
        "0111", "Дроссель", "%", gaugeMax = 100.0,
        about = "Положение дроссельной заслонки. На холостых — небольшой процент открытия; " +
            "завышенное значение бывает при грязной заслонке или сбитой адаптации.",
        norm = "ХХ 12–18 %",
        decode = { d -> d[0] * 100 / 255.0 },
    ),
    STFT(
        "0106", "Топл. коррекция", "%", gaugeMax = 25.0,
        warnHigh = 10.0, critHigh = 25.0, warnLow = -10.0, critLow = -25.0,
        about = "Кратковременная топливная коррекция. Показывает, насколько ЭБУ подмешивает или " +
            "убирает топливо. Стабильный плюс — бедная смесь (подсос), стабильный минус — богатая.",
        norm = "±10 %",
        decode = { d -> d[0] / 1.28 - 100 },
    ),
    TIMING(
        "010E", "УОЗ", "°", gaugeMax = 40.0,
        about = "Угол опережения зажигания. ЭБУ подстраивает его под нагрузку и детонацию; " +
            "нестабильность связана с качеством топлива или датчиком детонации.",
        norm = "ХХ 8–16°",
        decode = { d -> d[0] / 2.0 - 64 },
    ),
    MAP(
        "010B", "MAP", "кПа", gaugeMax = 250.0,
        about = "Абсолютное давление во впускном коллекторе. На холостых — глубокий вакуум " +
            "(низкое значение); растёт с нагрузкой. Помогает оценить герметичность впуска.",
        norm = "ХХ 25–45 кПа",
        decode = { d -> d[0].toDouble() },
    ),
    FUEL_LEVEL(
        "012F", "Топливо", "%", gaugeMax = 100.0, warnLow = 12.0, critLow = 5.0,
        about = "Уровень топлива в баке по датчику. Значение приблизительное и зависит от " +
            "калибровки датчика.",
        norm = "0–100 %",
        decode = { d -> d[0] * 100 / 255.0 },
    ),
    VOLTAGE(
        "0142", "Напряжение", "В", gaugeMax = 16.0,
        warnHigh = 14.8, critHigh = 15.3, warnLow = 12.2, critLow = 11.8,
        about = "Напряжение бортовой сети на модуле управления. При работающем двигателе " +
            "показывает зарядку от генератора: ниже 13 В — слабый генератор, выше 15 В — " +
            "неисправный регулятор.",
        norm = "12.4–14.7 В",
        decode = { d -> (256 * d[0] + d[1]) / 1000.0 },
    ),
}
