package ru.ngscanner.obd

/**
 * Часто используемые OBD-II параметры (Mode 01) и их запросы.
 * `pid` — байты запроса (режим + PID) в hex без пробелов.
 */
enum class ObdPid(val pid: String, val label: String) {
    ENGINE_RPM("010C", "Обороты двигателя"),
    VEHICLE_SPEED("010D", "Скорость"),
    COOLANT_TEMP("0105", "Температура охлаждающей жидкости"),
    ENGINE_LOAD("0104", "Расчётная нагрузка двигателя"),
    INTAKE_TEMP("010F", "Температура воздуха на впуске"),
    MAF("0110", "Массовый расход воздуха"),
    THROTTLE_POS("0111", "Положение дроссельной заслонки"),
    FUEL_TRIM_SHORT("0106", "Кратковременная топливная коррекция"),
    FUEL_TRIM_LONG("0107", "Долговременная топливная коррекция"),
}
